package e_wallet;

public class Student {
	public int id;
	public String name;
	public String bits_id;
	public String password;
	public int pin;
	public int ewbalance;
	public int credit_avail;
	public int swdbalance;
	public String log;
	
	
	public String getLog() {
		return log;
	}
	public void setLog(String log) {
		this.log = log;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBits_id() {
		return bits_id;
	}
	public void setBits_id(String bits_id) {
		this.bits_id = bits_id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getPIN() {
		return pin;
	}
	public void setPIN(int pin) {
		this.pin = pin;
	}
	public int getEWBalance() {
		return ewbalance;
	}
	public void setEWBalance(int ewbalance) {
		this.ewbalance = ewbalance;
	}
	public int getCredit_avail() {
		return credit_avail;
	}
	public void setCredit_avail(int credit_avail) {
		this.credit_avail = credit_avail;
	}
	public int getSwdbalance() {
		return swdbalance;
	}
	public void setSwdbalance(int swdbalance) {
		this.swdbalance = swdbalance;
	}
	
}
